package oop;

public class classDefinition {

	// [1] 클래스란?
	// 클래스란 객체(또는 인스턴스)를 생성하는 하나의 공장
	// 시골 허허벌판에 농기계를 만들어내는 공장(class)가 들어섰고, 농부들이 주문을 넣으면 농기계가 만들어져 나온다
	
	// [2] 클래스(공장)를 통해 객체를 어떻게 만들어내는지?
	// 만들고자 하는 객체의 "특징"과 "동작"에 많은 시간을 들이고 집중하면서 설계를 하게 된다
	
	// [3] 정리
	// 객체의 특징 --> 속성 (attribute)
	// 객체의 동작 --> 메서드 (method)
	// 파이썬에서는 일반적으로 함수라는 표현을 쓰지만, 클래스내에서의 함수는 메서드라고 부른다. 거의 함수와 동일하다.
}
